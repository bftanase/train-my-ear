package ro.btanase.chordlearning.exceptions;

public class OldConfigFileException extends RuntimeException {

  public OldConfigFileException() {
    super();
  }

  public OldConfigFileException(String message, Throwable cause) {
    super(message, cause);
  }

  public OldConfigFileException(String message) {
    super(message);
  }

  public OldConfigFileException(Throwable cause) {
    super(cause);
  }

}
